    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String linea;
        String[] datosBeneficio;

        BufferedReader bf = null;
        try {
            bf = new BufferedReader(new FileReader("C:\\PConceptoCapacidad\\archivos\\ArchivoCargaBeneficio.txt"));
            try {
                _matrixBeneficio = new ArrayList<BeneficiosBean>();
                
                while ((linea = bf.readLine()) != null) {//cambiar

                    datosBeneficio = linea.split(",");
                    System.out.println("---"+datosBeneficio[0]);
                    _matrixBeneficio.add(new BeneficiosBean(datosBeneficio[0], datosBeneficio[1], datosBeneficio[2], datosBeneficio[3],datosBeneficio[4],datosBeneficio[5]));
                    
                }


                for (int i = 0; i < _matrixBeneficio.size(); i++) {

                    if (i == 0) {
                        fechaUno.setText(_matrixBeneficio.get(i).getFecha());
                        bnoVentasUno.setText(String.valueOf(_matrixBeneficio.get(i).getBnoVentas()));
                        _VGastos1.setText(String.valueOf(_matrixBeneficio.get(i).getPorcGasto()));
                        _VCostos1.setText(String.valueOf(_matrixBeneficio.get(i).getPorcCostos()));
                    } else if (i == 1) {
                        fechaDos.setText(_matrixBeneficio.get(i).getFecha());
                        bnoVentasDos.setText(String.valueOf(_matrixBeneficio.get(i).getBnoVentas()));
                        _VGastos2.setText(String.valueOf(_matrixBeneficio.get(i).getPorcGasto()));
                        _VCostos2.setText(String.valueOf(_matrixBeneficio.get(i).getPorcCostos()));
                    } else if (i == 2) {
                        fechaTres.setText(_matrixBeneficio.get(i).getFecha());
                        bnoVentasTres.setText(String.valueOf(_matrixBeneficio.get(i).getBnoVentas()));
                        _VGastos3.setText(String.valueOf(_matrixBeneficio.get(i).getPorcGasto()));
                        _VCostos3.setText(String.valueOf(_matrixBeneficio.get(i).getPorcCostos()));
                    }

                }



            } catch (IOException ex) {
                Logger.getLogger(Deudas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NumberFormatException nb) {
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Deudas.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                bf.close();
            } catch (IOException ex) {
                Logger.getLogger(Deudas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_formWindowOpened

