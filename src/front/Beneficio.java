    private void jButtonVentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVentasActionPerformed
        if (FuncionesUtiles.isVacio(fechaUno.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha primera puede ser vacia");

        } else if (!FuncionesUtiles.isFormatoFecha(fechaUno.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha primera  debe contener el siguiente formato ##/##/####");

        } else if (FuncionesUtiles.isVacio(fechaDos.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha segunda puede ser vacia");

        } else if (!FuncionesUtiles.isFormatoFecha(fechaDos.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha segunda  debe contener el siguiente formato ##/##/####");

        } else if (FuncionesUtiles.isVacio(fechaTres.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha tercera puede ser vacia");

        } else if (!FuncionesUtiles.isFormatoFecha(fechaTres.getText())) {
            JOptionPane.showMessageDialog(null, "La fecha tercera  debe contener el siguiente formato ##/##/####");

        } else if (FuncionesUtiles.isVacio(bnoVentasUno.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Venta Uno puede ser vacia");

        } else if (!FuncionesUtiles.isNumerico(bnoVentasUno.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Ventas Dos debe ser númerico");

        } else if (FuncionesUtiles.isVacio(bnoVentasDos.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Venta Dos puede ser vacia");

        } else if (!FuncionesUtiles.isNumerico(bnoVentasDos.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Ventas Dos debe ser númerico");

        } else if (FuncionesUtiles.isVacio(bnoVentasTres.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Venta Tres puede ser vacia");

        } else if (!FuncionesUtiles.isNumerico(bnoVentasTres.getText())) {
            JOptionPane.showMessageDialog(null, "El Bno Ventas Tres debe ser númerico");

        } else {
            Ventas ventas = new Ventas();
            ventas.setLocationRelativeTo(null);
            ventas.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButtonVentasActionPerformed

